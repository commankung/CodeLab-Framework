<script language="javascript">

$(document).ready(function()
{
	$("#formLg").submit(function()
	{
		//remove all the class add the messagebox classes and start fading
		$("#msgbox").removeClass().addClass('messagebox').text('Validating...').fadeIn(300);
		//check the username exists or not from ajax
		$.post("conIdap.php",{ user_name:$('#username').val(),password:$('#pass').val(),rand:Math.random() } ,function(data)
        {
		  if(data=='yes') //if correct login detail
		  {
		  	$("#msgbox").fadeTo(200,0.1,function()  //start fading the messagebox
			{ 
			  //add message and change the class of the box and start fading
			  $(this).html('Logging in...').addClass('messageboxok').fadeTo(300,1,
              function()
			  { 
			  	 //redirect to secure page
				 document.location='main-admin.php?m=course';
				 
			  });
			  
			});
		  }
		  else 
		  {
		  	$("#msgbox").fadeTo(200,0.1,function() //start fading the messagebox
			{ 
			  //add message and change the class of the box and start fading
			  $(this).html('Login Incorrect!!!').addClass('messageboxerror').fadeTo(300,1);
			});		
          }
				
        });
 		return false; //not to post the  form physically
	});
	//now call the ajax also focus move from 
	$("#password").blur(function()
	{
		$("#formLg").trigger('submit');
	});
});
</script>
        
